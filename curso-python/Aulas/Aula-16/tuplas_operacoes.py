# Criando uma tupla com diferentes tipos de dados
pessoa = ("João", 17, "M", 50)

# Deletando a tupla inteira
del(pessoa)

# Tentativa de deletar um item da tupla (isso não é permitido!)
# del(pessoa[0])  # Isso geraria um erro!

# Imutabilidade: não podemos modificar uma tupla após criá-la, mas podemos deletá-la inteira com `del`.
