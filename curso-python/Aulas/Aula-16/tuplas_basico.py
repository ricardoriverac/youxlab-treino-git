# Aprendendo sobre Tuplas em Python

# Em Python, uma tupla é um tipo de estrutura de dados que permite armazenar múltiplos valores em uma única variável.
# Diferente das listas, as tuplas são imutáveis, ou seja, depois de criadas, seus valores não podem ser alterados.

# Sintaxe:
# () -> Tupla
# [] -> Lista
# {} -> Dicionário

# Criando uma tupla
lanche = ("Hambúrguer", "Suco", "Pizza", "Sorvete")

# Exibindo a tupla completa
print("lanche:", lanche)  # Saída: ('Hambúrguer', 'Suco', 'Pizza', 'Sorvete')

# Verificando o tipo da variável
print("Tipo da variável lanche:", type(lanche))  # Saída: <class 'tuple'>
