// var e let são usadas para criar variáveis
// A diferença é que:
// - var funciona em toda a função (ou fora, se estiver no topo do código)
// - let só funciona dentro do bloco onde foi criada (como dentro de if ou for)
// - var pode ser criada mais de uma vez, let não deixa
// - var pode ser usada antes de ser declarada (mas fica undefined)
// - let dá erro se tentar usar antes de declarar
// const não pode ser alterada ao longo do meu programa