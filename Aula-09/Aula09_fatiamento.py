#Manipulando textos

#Fatiamento
frase = "Aprendendo a programar em python."

print(frase[2]) #Pega apenas uma letra da frase
print(frase[2:10]) #Pega da letra 2 ate a 10
print(frase[2:10:2]) #O 2 No final, indica que ele vai pular de 2 em 2
print(frase[:10]) #Começa deis do inicio e termina no 10
print(frase[9::3]) #Começa do nove e pula de 3 em 3
