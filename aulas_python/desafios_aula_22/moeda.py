def metade(n):
    return n /2

def dobro(n):
    return n * 2

def aumentar(preco, porcentagem):
    aumento = preco * (porcentagem / 100)
    novo_preco = preco + aumento
    return novo_preco

def diminuir(preco, porcentagem):
    diminuicao = preco * (porcentagem / 100)
    novo_precoS = preco - diminuicao
    return novo_precoS

