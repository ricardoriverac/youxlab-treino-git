def metade(num=0.0):
    return num / 2
def dobro(num=0.0):
    return num * 2
def aumentar(num=00, aum=0):
    aumento = ((num * aum) / 100) + num
    return aumento

def diminuir(num=00, aum=0):
    resultado = num - ((num * aum) / 100)
    return resultado

print(metade(5.0))
print(dobro(5))
print(aumentar(10))
print(diminuir(1000))